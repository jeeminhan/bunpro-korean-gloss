# Tilde-insensitive index: collapse ～(FF5E)/〜(301C) and drop a leading wave dash
def tkey(s):
    return s.replace("～", "~").replace("〜", "~").lstrip("~")
by_tkey = {tkey(t): v for t, v in by_title.items()}

out = []
missing = []
for title, ko in KO.items():
    s = by_title.get(title) or by_tkey.get(tkey(title))
    if not s:
        missing.append(title)
        continue
    title = s["title"]  # use the source's canonical spelling as the key
    e = {"title": title, "level": s["level"], "ko": ko}
    if "id" in s:
        e["id"] = s["id"]
    out.append(e)

out.sort(key=lambda x: (x["level"], x["title"]))
json.dump(out, open(os.path.join(ROOT, "src/glosses.json"), "w"), ensure_ascii=False, indent=1)

n3 = sum(1 for x in out if x["level"] == "N3")
n2 = sum(1 for x in out if x["level"] == "N2")
print(f"emitted {len(out)} glosses  (N3={n3}, N2={n2}, with_id={sum(1 for x in out if 'id' in x)})")
